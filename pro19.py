# Understanding Statistical Operations
import numpy as np
print ("\n===== Statistical Operations =====\n\n")
a = np.array ([10, 20, 30, 40, 50])

#mean
print ("Median -> a.mean():", a.mean())

#median
print ("Median -> np.median():", np.median(a))

#min
print ("Minimum -> a.min():", a.min())

#max
print ("Maximum -> a.max():", a.max())

#sum
print ("Sum -> a.sum():", a.sum())

#standard deviation
print ("Standard Deviation -> a.std():", a.std())