# Understanding variables, input (), print (), type() functions...

print ("\nData-types, Variables and input (), print () & type () functions:\n\n")
name = input ("Enter Name: ")
print (name)
print (type (name))

i = int (input ("\nEnter any integer: "))
print (i)

print (type (i))

f = float (input ("\nEnter any floating number: "))
print (f)
print (type (f))

b = bool (input ("\nIs 2 + 2 = 4 ? State True or False: "))
print (b)
print (type (b))
